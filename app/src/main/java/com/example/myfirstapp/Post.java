package com.example.myfirstapp;

public class Post {
    private final int i;
    public int _id;
    public String about;
    public int index;
    public String name;
    public int thumbdown;
    public int thumbup;
    public String time;
    public String title;

    public Post(int i, String about, int index, String name, int thumbdown, int thumbup, String time, String title){

        this.i = _id;
        this.about = about;
        this.index = index;
        this.name = name;
        this.thumbdown = thumbdown;
        this.thumbup = thumbup;
        this.time = time;
        this.title = title;
    }

}
